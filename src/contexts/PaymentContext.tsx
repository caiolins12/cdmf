  const getFinancialSummary = useCallback(async (month?: string): Promise<FinancialSummary> => {
    const currentMonth = month || new Date().toISOString().slice(0, 7);
    
    // Busca faturas do mês
    const invoices = await fetchInvoices({ month: currentMonth });
    
    // Busca transações do mês
    const transactions = await fetchTransactions({ month: currentMonth });

    const paid = invoices.filter(i => i.status === "paid");
    const pending = invoices.filter(i => i.status === "pending");
    const overdue = invoices.filter(i => i.status === "overdue");

    // Calcula valores brutos (antes das taxas)
    const totalReceived = transactions
      .filter(t => t.type === "income")
      .reduce((sum, t) => sum + t.amount, 0);

    // Calcula total de taxas deduzidas e valor líquido recebido (valor que está na conta)
    const incomeTransactions = transactions.filter(t => t.type === "income");

    // Busca configurações de pagamento para calcular taxas de transações antigas
    const settings = await getPaymentSettings();
    
    const totalFees = incomeTransactions.reduce((sum, t) => {
      // Se já tem fee calculado, usa ele
      if (t.fee !== undefined && t.fee !== null) {
        return sum + t.fee;
      }
      // Se não tem fee mas é pagamento via Mercado Pago, calcula a taxa
      if (t.paymentMethod === "pix_mercadopago" || t.paymentMethod === "card") {
        const { fee } = calculateMercadoPagoFee(t.amount, t.paymentMethod || "pix_mercadopago", settings);
        return sum + fee;
      }
      return sum;
    }, 0);
    
    const totalReceivedNet = incomeTransactions.reduce((sum, t) => {
      // Usa amountNet se disponível (valor líquido após taxa)
      if (t.amountNet !== undefined && t.amountNet !== null) {
        return sum + t.amountNet;
      }
      // Se não tem amountNet mas é pagamento via Mercado Pago, calcula o valor líquido
      if (t.paymentMethod === "pix_mercadopago" || t.paymentMethod === "card") {
        const { amountNet } = calculateMercadoPagoFee(t.amount, t.paymentMethod || "pix_mercadopago", settings);
        return sum + amountNet;
      }
      // Para outros métodos de pagamento (sem taxa), usa o valor bruto
      return sum + t.amount;
    }, 0);

    const totalExpenses = transactions
      .filter(t => t.type === "expense")
      .reduce((sum, t) => sum + t.amount, 0);

    const totalPending = pending.reduce((sum, i) => sum + i.amount, 0);
    const totalOverdue = overdue.reduce((sum, i) => sum + i.amount, 0);

    return {
      totalReceived, // valor bruto recebido (antes das taxas)
      totalFees, // total de taxas deduzidas
      totalReceivedNet, // valor líquido recebido (após taxas) - valor que está na conta do banco
      totalPending,
      totalOverdue,
      totalExpenses,
      balance: totalReceivedNet - totalExpenses, // saldo líquido (valor na conta - despesas)
      invoicesCount: {
        paid: paid.length,
        pending: pending.length,
        overdue: overdue.length,
      },
    };
  }, [fetchInvoices, fetchTransactions, getPaymentSettings, calculateMercadoPagoFee]);

